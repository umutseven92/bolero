package com.bolero.game.data;

public class CharacterValues {
  public float width;
  public float height;
  public float maxVelocity;
  public float speed;

  public CharacterValues(float width, float height, float maxVelocity, float speed) {
    this.width = width;
    this.height = height;
    this.maxVelocity = maxVelocity;
    this.speed = speed;
  }
}
